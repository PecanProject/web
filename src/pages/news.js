                                    <p>We’ve added support for R Shiny visualizations in a way that we hope will
                                        allow users to easily add new Shiny apps to the catalog of PEcAn
                                        visualizations. Simply drop a new app into the pecan/shiny folder and it
                                        should go live. We’ve initially populated our Shiny catalog with two
                                        visualizations:</p>

